# ── 5. Compliance Score Breakdown ─────────────────────────────────────────────
@router.get("/compliance/{document_id}")
def compliance_breakdown(
    document_id: int,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    doc, analysis = _get_doc_and_analysis(db, document_id, current_user)
    risks = json.loads(analysis.risks_json or "[]")

    severity_counts = {"Critical": 0, "High": 0, "Medium": 0, "Low": 0}
    for r in risks:
        sev = r.get("severity", "Low")
        severity_counts[sev] = severity_counts.get(sev, 0) + 1

    clauses_present = sum(1 for f in [
        analysis.payment_terms, analysis.renewal_clause,
        analysis.confidentiality_clause, analysis.termination_clause,
        analysis.responsibilities,
    ] if f)
    clause_pct = round(clauses_present / 5 * 100)
    score = analysis.compliance_score or 0

    return {
        "document": doc.filename,
        "overall_compliance_score": score,
        "risk_score": analysis.risk_score,
        "severity_breakdown": severity_counts,
        "total_issues": len(risks),
        "clause_coverage_percent": clause_pct,
        "clauses_found": clauses_present,
        "clauses_total": 5,
        "grade": (
            "A" if score >= 85 else
            "B" if score >= 70 else
            "C" if score >= 55 else
            "D" if score >= 40 else "F"
        ),
    }